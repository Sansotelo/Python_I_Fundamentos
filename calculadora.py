"""
Atividade Calculadora
"""

numa = float(input("Primeiro valor: "))
numb = float(input("Segundo valor: "))

print(f"{numa} + {numb} = {numa+numb}")
print(f"{numa} - {numb} = {numa-numb}")
print(f"{numa} x {numb} = {numa*numb}")
print(f"{numa} / {numb} = {numa/numb:.2f}")
print(f"{numa} // {numb} = {numa//numb}")
print(f"{numa} % {numb} = {numa%numb}")
print(f"{numa} ^ {numb} = {numa**numb}")
